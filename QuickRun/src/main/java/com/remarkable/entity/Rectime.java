package com.remarkable.entity;

public class Rectime {
	
	//接收时间id
	private int rec_id;
	//详细接收时间
	private String rec_detail;
	//状态
	private int rec_state;
	
	public int getRec_id() {
		return rec_id;
	}
	public void setRec_id(int rec_id) {
		this.rec_id = rec_id;
	}
	public String getRec_detail() {
		return rec_detail;
	}
	public void setRec_detail(String rec_detail) {
		this.rec_detail = rec_detail;
	}
	public int getRec_state() {
		return rec_state;
	}
	public void setRec_state(int rec_state) {
		this.rec_state = rec_state;
	}
	

}
